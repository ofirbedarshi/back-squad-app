/**
 * Reads clouds-generation-a.xlsx and clouds-generation-b.xlsx; writes lookup TS files.
 *
 * Run: npm run generate:clouds-lookup
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import XLSX from 'xlsx'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.resolve(__dirname, '..')

function parseMetersToken(token) {
  const paren = token.match(/^\((-?\d+)\)m$/)
  if (paren) return Number(paren[1])
  const plain = token.match(/^(-?\d+)m$/)
  if (plain) return Number(plain[1])
  throw new Error(`Cannot parse meters token: ${token}`)
}

function parseHeightBandLabelGenA(label) {
  const trimmed = String(label).trim()
  const match = trimmed.match(/^(\([^)]+\)m|-?\d+m)-(\([^)]+\)m|-?\d+m)$/)
  if (!match) throw new Error(`Invalid height band label: ${label}`)
  const min = parseMetersToken(match[1])
  const max = parseMetersToken(match[2])
  if (min >= max) throw new Error(`Height band min must be < max: ${label}`)
  const id = `h_${min}_${max}`
  return { id, min, max, label: trimmed }
}

function parseHeightBandLabelGenB(label) {
  const trimmed = String(label).trim()
  let min
  let max

  if (trimmed.includes(' - ')) {
    const parts = trimmed.split(' - ').map((p) => Number(p.trim()))
    if (parts.length !== 2 || parts.some((n) => !Number.isFinite(n))) {
      throw new Error(`Invalid GenB height band label: ${label}`)
    }
    ;[min, max] = parts
  } else {
    const match = trimmed.match(/^(-?\d+(?:\.\d+)?)-(-?\d+(?:\.\d+)?)$/)
    if (!match) throw new Error(`Invalid GenB height band label: ${label}`)
    min = Number(match[1])
    max = Number(match[2])
  }

  if (min > max) {
    ;[min, max] = [max, min]
  }
  if (min >= max) throw new Error(`Height band min must be < max: ${label}`)
  const id = `h_${min}_${max}`
  return { id, min, max, label: trimmed }
}

function parseRangeHeaderGenA(header) {
  const trimmed = String(header).trim()
  const match = trimmed.match(/^(\d+(?:\.\d+)?)km-(\d+(?:\.\d+)?)km\s+(lofted|low)$/i)
  if (!match) throw new Error(`Invalid range column header: ${header}`)
  const min = Math.round(Number(match[1]) * 1000)
  const max = Math.round(Number(match[2]) * 1000)
  const trajectory = match[3].toLowerCase()
  if (min >= max) throw new Error(`Range band min must be < max: ${header}`)
  const id = `r_${min}_${max}`
  return { id, min, max, trajectory, label: trimmed }
}

function parseRangeLabelGenB(label) {
  const trimmed = String(label).trim()
  const numbers = [...trimmed.matchAll(/\d+(?:\.\d+)?/g)].map((m) => Number(m[0]))
  if (numbers.length < 2) {
    throw new Error(`Invalid GenB range label: ${label}`)
  }
  const minKm = Math.min(numbers[0], numbers[1])
  const maxKm = Math.max(numbers[0], numbers[1])
  const min = Math.round(minKm * 1000)
  const max = Math.round(maxKm * 1000)
  if (min >= max) throw new Error(`Range band min must be < max: ${label}`)
  const id = `r_${min}_${max}`
  return { id, min, max, label: trimmed }
}

function parseTrajectoryGenB(header) {
  const trimmed = String(header).trim().toLowerCase()
  if (trimmed === 'lw') return 'low'
  if (trimmed === 'l') return 'lofted'
  if (trimmed === 'l+') return 'loftedPlus'
  throw new Error(`Invalid GenB trajectory header: ${header}`)
}

function writeGeneratedTs({ outputPath, exportName, timestampExportName, sourceBasename, sheetName, data }) {
  const lines = [
    '// AUTO-GENERATED by scripts/generate-clouds-lookup.mjs — do not edit',
    `// Source: data/fire-feasibility/${sourceBasename} (sheet: ${sheetName})`,
    '',
    "import type { CloudsFeasibilityLookupData } from './cloudsFeasibility.types.ts'",
    '',
    `export const ${timestampExportName} = ${JSON.stringify(new Date().toISOString())}`,
    '',
    `export const ${exportName}: CloudsFeasibilityLookupData = `,
    JSON.stringify(data, null, 2),
    '',
  ]
  fs.writeFileSync(outputPath, lines.join('\n'))
  console.log(`Wrote ${outputPath}`)
  console.log(`  height bands: ${data.heightBands.length}`)
  console.log(`  range bands: ${data.rangeBands.length}`)
  console.log(`  lookup cells: ${Object.keys(data.lookup).length}`)
}

function generateGenA() {
  const inputPath = path.join(root, 'data/fire-feasibility/clouds-generation-a.xlsx')
  const outputPath = path.join(root, 'src/domain/cloudsFeasibilityLookup.generated.ts')

  if (!fs.existsSync(inputPath)) {
    console.error(`Missing input file: ${inputPath}`)
    process.exit(1)
  }

  const workbook = XLSX.readFile(inputPath)
  const sheetName = workbook.SheetNames[0]
  const sheet = workbook.Sheets[sheetName]
  const rows = XLSX.utils.sheet_to_json(sheet, { header: 1, defval: '' })

  if (rows.length < 2) {
    throw new Error('GenA sheet has no data rows')
  }

  const headerRow = rows[0]
  const heightHeader = String(headerRow[0] ?? '').trim()
  if (!heightHeader) {
    throw new Error('Missing height band header in column A')
  }

  const rangeColumns = []
  for (let col = 1; col < headerRow.length; col++) {
    const header = headerRow[col]
    if (!header) continue
    rangeColumns.push({ col, ...parseRangeHeaderGenA(header) })
  }

  if (rangeColumns.length === 0) {
    throw new Error('No range columns found in GenA header row')
  }

  const heightBands = []
  const lookup = {}

  for (let rowIndex = 1; rowIndex < rows.length; rowIndex++) {
    const row = rows[rowIndex]
    const heightLabel = row[0]
    if (!heightLabel) continue

    const heightBand = parseHeightBandLabelGenA(heightLabel)
    if (heightBands.some((b) => b.id === heightBand.id)) {
      throw new Error(`Duplicate height band: ${heightBand.label}`)
    }
    heightBands.push(heightBand)

    for (const { col, id: rangeId, trajectory } of rangeColumns) {
      const raw = row[col]
      if (raw === '' || raw === null || raw === undefined) continue

      const value = typeof raw === 'number' ? raw : Number(String(raw).trim())
      if (!Number.isFinite(value)) {
        throw new Error(`Non-numeric cell at row ${rowIndex + 1}, col ${col + 1}: ${raw}`)
      }

      const key = `${heightBand.id}|${rangeId}`
      if (!lookup[key]) lookup[key] = {}
      if (lookup[key][trajectory] !== undefined) {
        throw new Error(`Duplicate lookup for ${heightBand.label} / ${rangeId} / ${trajectory}`)
      }
      lookup[key][trajectory] = value
    }
  }

  const rangeBandMap = new Map()
  for (const col of rangeColumns) {
    if (!rangeBandMap.has(col.id)) {
      const baseLabel = col.label.replace(/\s+(lofted|low)$/i, '')
      rangeBandMap.set(col.id, { id: col.id, min: col.min, max: col.max, label: baseLabel })
    }
  }
  const rangeBands = [...rangeBandMap.values()].sort((a, b) => a.min - b.min)

  writeGeneratedTs({
    outputPath,
    exportName: 'cloudsFeasibilityLookupData',
    timestampExportName: 'CLOUDS_FEASIBILITY_LOOKUP_GENERATED_AT',
    sourceBasename: path.basename(inputPath),
    sheetName,
    data: { heightBands, rangeBands, lookup },
  })
}

function generateGenB() {
  const inputPath = path.join(root, 'data/fire-feasibility/clouds-generation-b.xlsx')
  const outputPath = path.join(root, 'src/domain/cloudsFeasibilityGenBLookup.generated.ts')

  if (!fs.existsSync(inputPath)) {
    console.error(`Missing input file: ${inputPath}`)
    process.exit(1)
  }

  const workbook = XLSX.readFile(inputPath)
  const sheetName = workbook.SheetNames[0]
  const sheet = workbook.Sheets[sheetName]
  const rows = XLSX.utils.sheet_to_json(sheet, { header: 1, defval: '' })

  if (rows.length < 4) {
    throw new Error('GenB sheet has too few rows')
  }

  const trajectoryRow = rows[rows.length - 2]
  const rangeRow = rows[rows.length - 1]
  const heightCol = 1

  const rangeColumns = []
  const maxCol = Math.max(trajectoryRow.length, rangeRow.length)
  for (let col = 0; col < maxCol; col++) {
    const trajectoryHeader = trajectoryRow[col]
    const rangeHeader = rangeRow[col]
    if (!trajectoryHeader || !rangeHeader) continue

    let trajectory
    let rangeBand
    try {
      trajectory = parseTrajectoryGenB(trajectoryHeader)
      rangeBand = parseRangeLabelGenB(rangeHeader)
    } catch {
      continue
    }

    const { id: rangeId, min, max, label: rangeLabel } = rangeBand
    rangeColumns.push({ col, rangeId, min, max, rangeLabel, trajectory })
  }

  if (rangeColumns.length === 0) {
    throw new Error('No range/trajectory columns found in GenB footer rows')
  }

  const heightBands = []
  const lookup = {}

  for (let rowIndex = 1; rowIndex < rows.length - 2; rowIndex++) {
    const row = rows[rowIndex]
    const heightLabel = row[heightCol]
    if (!heightLabel) continue

    const heightBand = parseHeightBandLabelGenB(heightLabel)
    if (heightBands.some((b) => b.id === heightBand.id)) {
      throw new Error(`Duplicate height band: ${heightBand.label}`)
    }
    heightBands.push(heightBand)

    for (const { col, rangeId, trajectory } of rangeColumns) {
      const raw = row[col]
      if (raw === '' || raw === null || raw === undefined) continue

      const value = typeof raw === 'number' ? raw : Number(String(raw).trim())
      if (!Number.isFinite(value)) {
        throw new Error(`Non-numeric cell at row ${rowIndex + 1}, col ${col + 1}: ${raw}`)
      }

      const key = `${heightBand.id}|${rangeId}`
      if (!lookup[key]) lookup[key] = {}
      if (lookup[key][trajectory] !== undefined) {
        throw new Error(`Duplicate lookup for ${heightBand.label} / ${rangeId} / ${trajectory}`)
      }
      lookup[key][trajectory] = value
    }
  }

  const rangeBandMap = new Map()
  for (const col of rangeColumns) {
    if (!rangeBandMap.has(col.rangeId)) {
      rangeBandMap.set(col.rangeId, {
        id: col.rangeId,
        min: col.min,
        max: col.max,
        label: col.rangeLabel,
      })
    }
  }
  const rangeBands = [...rangeBandMap.values()].sort((a, b) => a.min - b.min)

  writeGeneratedTs({
    outputPath,
    exportName: 'cloudsFeasibilityGenBLookupData',
    timestampExportName: 'CLOUDS_FEASIBILITY_GEN_B_LOOKUP_GENERATED_AT',
    sourceBasename: path.basename(inputPath),
    sheetName,
    data: { heightBands, rangeBands, lookup },
  })
}

generateGenA()
generateGenB()
