/**
 * Reads raw_lahat_data_excel.xlsx and writes shared feasibility lookup TS file.
 *
 * Run: npm run generate:feasibility-lookup
 */
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'
import XLSX from 'xlsx'

const __dirname = path.dirname(fileURLToPath(import.meta.url))
const root = path.resolve(__dirname, '..')

const inputCandidates = [
  process.env.FEASIBILITY_LOOKUP_INPUT,
  path.join(root, 'data/fire-feasibility/raw_lahat_data_excel.xlsx'),
  '/Users/ofirbedarshi/Downloads/raw_lahat_data_excel.xlsx',
].filter(Boolean)

const outputPath = path.join(root, 'src/domain/feasibilityLookup.generated.ts')

const trajectoryByRawValue = {
  low: 'low',
  lofted: 'lofted',
  lofted_plus: 'loftedPlus',
}

function resolveInputPath() {
  const inputPath = inputCandidates.find((candidate) => fs.existsSync(candidate))
  if (!inputPath) {
    throw new Error(`Missing input file. Checked: ${inputCandidates.join(', ')}`)
  }
  return inputPath
}

function toFiniteNumber(value, label, rowIndex) {
  const parsed = typeof value === 'number' ? value : Number(String(value).trim())
  if (!Number.isFinite(parsed)) {
    throw new Error(`Invalid ${label} at row ${rowIndex + 2}: ${value}`)
  }
  return parsed
}

function toRoundedMeter(value) {
  return Math.round(value)
}

function buildKey(rangeMeters, positionToTargetHeightDiffMeters, positionToObstacleRangeMeters) {
  return `${rangeMeters}|${positionToTargetHeightDiffMeters}|${positionToObstacleRangeMeters}`
}

function writeGeneratedTs({ inputPath, sheetName, data }) {
  const lines = [
    '// AUTO-GENERATED by scripts/generate-feasibility-lookup.mjs — do not edit',
    `// Source: ${path.relative(root, inputPath)} (sheet: ${sheetName})`,
    '',
    "import type { FeasibilityLookupData } from './feasibilityLookup.types.ts'",
    '',
    `export const FEASIBILITY_LOOKUP_GENERATED_AT = ${JSON.stringify(new Date().toISOString())}`,
    '',
    'export const feasibilityLookupData: FeasibilityLookupData = ',
    JSON.stringify(data, null, 2),
    '',
  ]

  fs.writeFileSync(outputPath, lines.join('\n'))
  console.log(`Wrote ${outputPath}`)
  console.log(`  ranges: ${data.rangesMeters.length}`)
  console.log(`  target diff values: ${data.positionToTargetHeightDifferencesMeters.length}`)
  console.log(`  obstacle ranges: ${data.positionToObstacleRangesMeters.length}`)
  console.log(`  lookup cells: ${Object.keys(data.lookup).length}`)
}

function generate() {
  const inputPath = resolveInputPath()
  const workbook = XLSX.readFile(inputPath)
  const sheetName = workbook.SheetNames[0]
  const sheet = workbook.Sheets[sheetName]
  const rows = XLSX.utils.sheet_to_json(sheet, { defval: '' })

  const lookup = {}
  const rangesMeters = new Set()
  const positionToTargetHeightDifferencesMeters = new Set()
  const positionToObstacleRangesMeters = new Set()

  for (let rowIndex = 0; rowIndex < rows.length; rowIndex++) {
    const row = rows[rowIndex]
    const rawTrajectory = String(row.flight_type ?? '').trim().toLowerCase()
    const trajectory = trajectoryByRawValue[rawTrajectory]
    if (!trajectory) {
      throw new Error(`Unknown trajectory "${row.flight_type}" at row ${rowIndex + 2}`)
    }

    const rangeMeters = toRoundedMeter(toFiniteNumber(row['range טווח'], 'range', rowIndex))
    const positionToTargetHeightDiffMeters = toRoundedMeter(
      toFiniteNumber(row['diff_high  הפרש עמדה מהמטרה'], 'diff_high', rowIndex),
    )
    const positionToObstacleRangeMeters = toRoundedMeter(
      toFiniteNumber(row['dist_of_flight מרחק הטיל ברגע הזה מהעמדה'], 'dist_of_flight', rowIndex),
    )
    const missileHeightAbovePositionMeters = toRoundedMeter(
      toFiniteNumber(row['hight גובה מעל העמדה'], 'hight', rowIndex),
    )

    const key = buildKey(
      rangeMeters,
      positionToTargetHeightDiffMeters,
      positionToObstacleRangeMeters,
    )
    if (!lookup[key]) lookup[key] = {}
    if (lookup[key][trajectory] !== undefined) {
      throw new Error(`Duplicate lookup cell for key=${key}, trajectory=${trajectory}`)
    }
    lookup[key][trajectory] = missileHeightAbovePositionMeters

    rangesMeters.add(rangeMeters)
    positionToTargetHeightDifferencesMeters.add(positionToTargetHeightDiffMeters)
    positionToObstacleRangesMeters.add(positionToObstacleRangeMeters)
  }

  const data = {
    rangesMeters: [...rangesMeters].sort((a, b) => a - b),
    positionToTargetHeightDifferencesMeters: [...positionToTargetHeightDifferencesMeters].sort(
      (a, b) => a - b,
    ),
    positionToObstacleRangesMeters: [...positionToObstacleRangesMeters].sort((a, b) => a - b),
    lookup,
  }

  writeGeneratedTs({ inputPath, sheetName, data })
}

generate()
